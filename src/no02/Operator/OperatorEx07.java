public class OperatorEx07 
{
	public static void main(String[] args)
	{
		boolean power = false;
		System.out.println(power);
		power = !power;				//power의 값이 false에서 true로 바뀜
		System.out.println(power);
		power = !power;
		System.out.println(power);
		
		
	}
}
